const settingContainer: Ref<HTMLElement | null> = ref<HTMLElement | null>(null);

function showSettings() {
    if (store.showSetting)
        store.changeShowSetting(false);
    else store.changeShowSetting(true);
}

function closeSetting(event: MouseEvent) {
    if (settingContainer.value && !settingContainer.value.contains(event.target as Node) && store.showSetting) {
        store.changeShowSetting(false);
    }
}

function showLiveStatus() {
    store.changeShowLive(true);
}

onMounted(() => {
    document.addEventListener('click', closeSetting);
})

onUnmounted(() => {
